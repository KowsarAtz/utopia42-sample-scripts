async function main() {
    console.log("Running Builder");

    let pos = await UtopiaApi.getPlayerPosition();

    let x = Math.round(pos.x);
    let y = Math.round(pos.y) + 3;
    let z = Math.round(pos.z);

    console.log("Starting position: " + pos.x + ", " + pos.y + ", " + pos.z);

    for (const blockPos of vox.XYZI) {
        const xx = x + blockPos.x;
        const yy = y + blockPos.z;
        const zz = z + blockPos.y;

        let res = await UtopiaApi.placeBlock(Inputs.blockType, xx, yy, zz);
        console.log(
            "Placed block at " + xx + ", " + yy + ", " + zz + ": " + res
        );
    }
}
